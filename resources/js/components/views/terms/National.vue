                    </li>
                    <li>
                        Realiza las pruebas con una conexión alámbrica en lugar de utilizar el Wi-Fi de tu dispositivo.
                    </li>
                    <li>
                        Si encuentras diferencia en la medición de velocidades, deberás asegurarte de que el ancho de banda de tu servicio esté completamente libre, es decir, sin descargas al mismo tiempo, ni escuchando música o viendo videos y de igual forma asegurarse que nadie más se encuentre conectado.
                    </li>
                    <li>
                        Al realizar la prueba con un teléfono celular o dispositivo móvil, desconecta el servicio de datos 3G para que la prueba se realice utilizando exclusivamente la conexión Wi-Fi.
                    </li>
                    <li>
