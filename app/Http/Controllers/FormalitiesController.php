            GeneralController::saveTransactionLog(2,
                'El usuario con id: '.auth()->user()->id.' Crea un nuevo trÃ¡mite con id: '. $formality->id);

            DB::commit();
            return response()->json([
                'success' => true,
            ]);
        }
        catch ( \Exception $e ) {
            DB::rollBack();

            return response()->json([
                'success' => false,
                'message' => $e->getMessage()
            ]);
        }
    }

